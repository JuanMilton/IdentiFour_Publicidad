                            <p>La <b>cirugía oral</b> es la especialidad más antigua de la odontología. Se dedica al diagnóstico y tratamiento de las enfermedades, traumatismos, defectos de las piezas dentarias, de los maxilares y de los tejidos blandos adyacentes que requieran <b>intervención quirúrgica.</b> </p>
                            <p>El cirujano oral debe tener profundos conocimientos de ciencias básicas para su profesión como la anatomía, la histología y la fisiología, así como de las entidades nosológicas más frecuentes. Además de su capacitación, entrenamiento quirúrgico y obviamente una gran destreza manual, es indispensable que el profesional posee madurez de pensamiento y un gran respeto por los tejidos vivos. </p>
                            <p>Algunas de las Intervenciones más frecuentes son: </p>
                            <p><b>Extracción de cordales</b> o muelas del juicio es la intervención más frecuente en <b>cirugía oral</b>. Entre las muchas indicaciones para la extracción de estas piezas podemos citar la falta de espacio en la mandíbula para su correcta erupción, su orientación anómala, que puede producir daños en las piezas vecinas o apiñamientos dentarios, o problemas de inflamación en la encía por una erupción incompleta, que puede generar una infección crónica en la encía circundante.</p>
                            <p><b>Extracciones dentarias complejas (dientes incluidos o impactados).</b> A veces una extracción dentaria puede ser complicada desde el punto de vista quirúrgico. Por ejemplo si el paciente padece enfermedades sistémicas importantes, ha recibido radioterapia recientemente o sufre trastornos en la coagulación, si las piezas dentarias están destruidas, etc. En tal caso, el cirujano oral aporta su habilidad quirúrgica para solucionar el problema.</p>
                            <p><b>Cirugía preprotésica periodontales.</b> Son intervenciones que se realizan previamente a la colocación de una prótesis. Consisten en el acondicionamiento de la encía o del hueso maxilar o mandibular para facilitar una correcta adaptación de la prótesis. También podemos incluir en este tipo de <b>cirugía</b> la colocación de injertos de hueso para la colocación posterior de implantes.</p>
                            <p><b>Anestesia</b> El importante avance en las técnicas de <b>cirugía oral</b> e implantología ha ido acompañado de la introducción de la práctica <b>anestésica</b> que permite modificar un procedimiento quirúrgico que podría ser desagradable en una experiencia confortable. </p>
