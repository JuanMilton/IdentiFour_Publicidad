                            <h3>Clínica Dental Medic “LAS CONDES”</h3>
                            <p></p>
                            <p>La <b>odontopediatría</b> es la rama de la odontología encargada de <b>tratar los dientes de los niños</b>, desde el nacimiento a los 6 años cuidando sus dientes temporales, y hasta los 12 años cuando finaliza la dentición mixta y se inicia la definitiva.</p>
                            <p>Los <b>odontopediatras</b> se encargan de detectar precozmente las anomalías de la posición de los maxilares para remitir el paciente al ortodoncista y cirujano maxilofacial y así, poder modificar o frenar el crecimiento de estos huesos. Asimismo detecta los malos hábitos con el fin de reeducarlos, realiza un tratamiento restaurador en caso de caries dental o sellador para prevenir futura patología e informa sobre procedimientos de higiene oral para conseguir un crecimiento sano de la cavidad bucal.</p>
                            <h3>¿A partir de qué edad debería llevar a su hijo al dentista?</h3>
                            <p>Normalmente con la erupción del primer diente deciduo, a los 6 meses de vida. Desde este momento debería controlar la erupción dental y la higiene de su bebé, así como hacer controles periódicos a su especialista. Aproximadamente, a los 2-3 años su niño debería tener todos los dientes temporales en boca y a los 6 años le saldrá su primera muela definitiva. Entonces recomendamos una visita al ortodoncista por si fuese conveniente modificar el crecimiento de sus maxilares o crear espacio para el resto de dientes definitivos, ahora estamos a tiempo.</p>
                            <h3>¿A qué edad debe empezar a cepillarse los dientes su niño?</h3>
                            <p>Desde que erupciona su primer diente, habitualmente sucede a los 6 meses. Deberá limpiárselos con una gasita hasta los 2-3 años aproximadamente que ya tendrá todos los dientes temporales en boca. A esta edad el niño ya puede coger el cepillo y acompañándose de su mano seguir un cepillado correcto. A los 6 años el niño ya debería cepillárselos solito y posteriormente que un adulto le revise para asegurarnos de que utiliza la técnica adecuada y sus dientes quedan limpios. El niño debe aprender de pequeño a coger hábitos y responsabilidad sobre su higiene oral, y una vez adquirida mantendrá sus dientes para toda la vida.</p>
                            <h3>¿Qué hacer si tu diente salta de la boca tras un golpe?</h3>
                            <p>A veces, una rápida actuación puede hacer posible la recuperación de ese diente. Conserve la calma, recójalo del suelo sujetándolo por la corona, no por la raíz y si es posible lávelo con suero fisiológico o agua, nunca use jabón ni cepille la raíz. Debe intentar colocar el diente en su sitio asegurándose que está en la posición correcta y acuda inmediatamente al dentista. Es más fácil de lo que parece pues la raíz del diente encaja perfectamente en el alveolo dentario (el hueco donde estaba el diente). Durante el trayecto mantenga el diente en un vaso de leche (a ser posible entera) o suero fisiológico. En último extremo podría llevarlo dentro de su boca junto a la mejilla o debajo de la lengua, en contacto con su saliva. El factor tiempo es lo más importante en el éxito del diente reimplantado.</p>
                            
