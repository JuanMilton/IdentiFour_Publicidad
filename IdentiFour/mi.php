                    <h3>¿Qué es la caries dental?</h3>
                    <p>La <b>caries</b> es una enfermedad multifactorial que se caracteriza por la destrucción de los tejidos duros del diente como consecuencia de la desmineralización provocada por los ácidos que genera la placa bacteriana a partir de los restos de alimentos.</p>
                    <h3>¿Cuál es el causante de la caries dental?</h3>
                    <p>La <b>caries dental</b> se asocia a una deficiente higiene oral, como errores en las técnicas de cepillado y ausencia de hilo dental, a una etiología genética predisponente, se estudia aún la influencia del pH de la saliva en relación a la <b>caries</b>, y a una dieta rica en azúcares. Es la interacción entre el huésped (la bacteria que coloniza), el substrato (el diente), y el factor tiempo, muy importante para la aparición y el desarrollo de la caries. </p>
                    <h3>¿Qué produce la caries dental?</h3>
                    <p>La <b>caries dental</b> produce la destrucción de los tejidos dentales. Tras la destrucción del esmalte ataca a la dentina hasta alcanzar la pulpa dentaria (el nervio del diente) produciendo su inflamación, pulpitis, y la posterior necrosis (muerte pulpar), la cual requiere ser tratada mediante la endodoncia. La <b>caries</b> es la primera causa de pérdida de dientes en las personas jóvenes. </p>
                    <h3>¿Por qué tratar la caries dental?</h3>
                    <p>Si el diente no es tratado la infección puede alcanzar la pulpa dental y llegar posteriormente a inflamar el área que rodea el ápice (extremo de la raíz del diente) produciéndose una periodontitis apical, y pudiendo llegar a ocasionar un absceso, una celulitis o incluso un flemón, pudiendo sufrir todos o algún estadio del dolor que corresponde. Cuando la <b>caries dental</b> se ha extendido a la pulpa deberemos realizar la endodoncia (el tratamiento de los conductos radiculares) y reconstruir el diente posiblemente con una corona, de modo que si llegamos a tiempo de tratar la caries evitaremos una posible endodoncia o extracción en el caso de que el diente no fuese restaurable. </p>
                    <h3>¿Cómo prevenir la caries dental?</h3>
                    <p>La <b>higiene oral</b> es necesaria para prevenir las <b>caries</b> y consiste en una adecuada técnica de cepillado dental, por lo menos dos veces al día, preferiblemente después de cada comida y en el momento antes de acostarse, usar la seda dental una vez al día y realizar enjuagues con flúor u otros antisépticos si se requiere. Además, es importante un examen riguroso de la cavidad oral y una exhaustiva limpieza bucal y acudir regularmente a visitas de control. </p>
