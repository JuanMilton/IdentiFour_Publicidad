                            <h3>Clínica Dental Medic “LAS CONDES”</h3>
                            <p></p>
                            <p>El <b>endodoncista</b> es el especialista de la odontología que estudia todo lo relacionado con el diagnóstico y el tratamiento de los conductos radiculares. </p>
                            <p>A pesar de que esta ciencia tiene ya muchos años de vida, el desarrollo de los instrumentos de titanio, la nueva maquinaria electrónica, las innovadoras técnicas radiográficas apoyadas en la informática, y sin duda, la excelente formación de nuevas hornadas de jóvenes profesionales, consigue éxitos que hace pocos años nos estaban vedados. </p>
                            <p>Por lo tanto, la aportación de los endodoncistas es imprescindible aunque hagan el trabajo que no se ve, pero que soporta la restauración de todo el sistema dentario y de esta forma el prostodoncista puede acabar con una estética saludable. </p>
                            <h3>¿Para qué sirve una endodoncia?</h3>
                            <p>La <b>endodoncia</b> nos permite salvar dientes con un grado de destrucción tal que sobrepasa el esmalte y la dentina, afectando a la pulpa o al nervio dentario. Se aplica en piezas dentales fracturadas, con caries profundas o con lesiones en su tejido pulpar. También está aconsejada la endodoncia en dientes que requieren tallados protésicos exhaustivos para la posterior colocación de coronas o puentes, y en dientes que presenten lesiones periodontales.</p>
                            <p>De esta manera podrán seguir cumpliendo sus funciones de estabilidad, masticación y estética.</p>
                            <h3>¿Qué síntomas tenemos cuando necesitamos una endodoncia?</h3>
                            <p>La sintomatología varía en cada caso, el diente se vuelve sensible al frío, al calor y a la masticación. El dolor puede ser intermitente o constante, incluso el diente puede cambiar de color o puede aparecer un flemón. </p>
                            <h3>¿En qué consiste una endodoncia?</h3>
                            <p>La <b>endodoncia</b> consiste en eliminar el tejido pulpar enfermo (infectado) del interior de los conductos del diente, limpiar y preparar (instrumentación e irrigación) estos conductos hasta el final de sus raíces (ápice) y posteriormente se rellenan con un material biocompatible para conseguir un buen sellado.</p>
                            <h3>¿Son más frágiles los dientes tras una endodoncia?</h3>
                            <p>El diente que no sea restaurado es susceptible a sufrir una fractura, por lo tanto deberá acudir a su odontólogo lo antes posible una vez finalizado el tratamiento de <b>endodoncia</b> para reconstruírselo.</p>
                            <p>En pocos casos un diente correctamente restaurado y tratado con <b>endodoncia</b> puede sufrir una fractura al cabo de un tiempo por pérdida de la restauración, caries u otros factores predisponentes entre los que se encuentra el bruxismo.</p>
                            <h3>¿Cuántas sesiones son necesarias?</h3>
                            <p>En la mayoría de los tratamientos la endodoncia puede realizarse en una sola sesión, si bien en algunos casos pueden ser necesarios o aconsejables dos o más sesiones, siempre a juicio del profesional que la realiza.</p>
                            <h3>¿Es doloroso el tratamiento de endodoncia?</h3>
                            <p>El tratamiento de <b>endodoncia</b> se realiza bajo el efecto de anestésicos locales. Acabada la endodoncia y pasado el efecto de la anestesia podría aparecer alguna molestia en la masticación durante unos días debido a la irritación del tejido periodontal que rodea la raíz. En estos casos se requeriría una medicación adecuada.</p>
