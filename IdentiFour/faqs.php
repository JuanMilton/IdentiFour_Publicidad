                            <h4>¿Que es la Periodoncia?</h4>
                            <p><b>Periodoncia</b> es la especialidad de la Odontología que estudia la <b>prevención, el diagnóstico y el tratamiento de las enfermedades o anomalías que afectan a los tejidos que soportan los dientes</b>. Las principales enfermedades periodontales son la <b>Gingivitis y la Periodontitis</b>.</p>

                            <h4>¿Qué es la Enfermedad Periodontal?</h4>
                            <p>Es una enfermedad que afecta a las encías y al resto de estructuras que soportan los dientes, la padece el 90% de la población por encima de los 35 años y es la primera causa de pérdida de dientes en la población mundial. </p>

                            <h4>¿Cuáles son las causas de la Enfermedad Periodontal?</h4>
                            <p>La bacteria presente en la placa causa la enfermedad periodontal. Si no se retira, cuidadosamente, todos los días con el cepillo y el hilo dental, la placa se endurece y se convierte en una substancia dura y porosa llamada cálculo (también conocida como sarro). Las toxinas, que se producen por la placa bacteriana irritan las encías. Al permanecer un tiempo en el mismo lugar, las toxinas provocan que las encías se desprendan de los dientes y se forman bolsas periodontales, las cuales se llenan de más toxinas y bacterias.</p>
                            <p>Conforme la enfermedad avanza, las bolsas se extienden y la placa penetra más y más hasta que el hueso que sostiene al diente se destruye. Eventualmente, el diente se caerá o necesitará ser extraído por falta de soporte óseo.</p>
                            <p>Pese a que las bacterias son las únicas que pueden causar la enfermedad periodontal, hay otros factores que pueden empeorarla como son: la genética, el tabaco, el estrés, la diabetes y la obesidad.</p>

                            <h4>¿Cuáles son las etapas de la enfermedad periodontal?</h4>
                            <p>
                            <div class="lists-check">
                                Existen muchas formas de enfermedad periodontal. Entre las más comunes se incluyen las siguientes:
                                <ul style="padding-left: 20px">
                                    <li><b>Gingivitis:</b> es la forma menos severa de enfermedad periodontal. Las encías se inflaman, se enrojecen y sangren fácil y frecuentemente (excepto en el paciente fumador). Normalmente hay poca, o ninguna, incomodidad en esta etapa. La gingivitis es reversible si es tratada profesionalmente y con un buen cuidado de la higiene oral en casa.</li>
                                    <li><b>Periodontitis inicial:</b> si la gingivitis no es tratada, puede progresar hacia una periodontitis. En esta etapa la enfermedad periodontal empieza a destruir el hueso y el tejido que sostiene a los dientes.</li>
                                    <li><b>Periodontitis moderada a avanzada: </b> se desarrolla si las primeras etapas de la enfermedad pasan desatendidas. Ésta es la forma más avanzada de la enfermedad en donde ocurre una extensa pérdida de hueso y tejido.</li>
                                    <li><b>Periodontitis agresiva: </b> se caracteriza por la rápida pérdida de hueso alrededor de los dientes en un breve espacio de tiempo. Se da una inflamación muy marcada sin excesiva acumulación de placa ni cálculo. En estos casos la predisposición genética es muy importante.</li>
                                </ul>
                            </div>
                            </p>

                            <h4>¿Qué síntomas y señales de alerta podemos notar?</h4>
                            <p>En ocasiones la enfermedad periodontal puede progresar sin ningún síntoma ni dolor. Durante una revisión dental regular, el dentista busca señales de la enfermedad periodontal, por lo que la enfermedad aún no detectada puede ser tratada antes de que avance. </p>
                            <p>
                            <div class="lists-check">
                                <ul style="padding-left: 20px">
                                    <li><b>Encías rojizas e inflamadas.</b></li>
                                    <li><b>Sangrado espontaneo</b>, al cepillarse o pasarse el hilo dental. </li>
                                    <li><b>Encías que se desprenden de los dientes, blandas</b></li>
                                    <li><b>Dientes flojos o separados.</b> </li>
                                    <li><b>Mal aliento continuo.</b> </li>
                                    <li><b>Aumento de la sensibilidad dentaria, sobre todo al frío.</b> </li>
                                    <li><b>Cambio en la forma en la que los dientes se encajan al morder y espacios interdentales.</b> </li>
                                    <li><b>Cambio en el ajuste de dentaduras parciales.</b> </li>
                                    <li><b>Pus entre la encía y el diente, abscesos y flemones.</b> </li>
                                </ul>
                            </div>
                            </p>

                            <h4>¿En qué se basa el tratamiento de la enfermedad periodontal?</h4>
                            <p>El objetivo del tratamiento es eliminar la infección que produce la enfermedad. Una vez controlada la infección, el proceso destructivo del hueso se detiene y de este modo se pueden conservar los dientes y los tejidos periodontales sanos. Dependiendo de la cantidad de hueso de soporte destruido, los dientes una vez tratados tendrán mejor o peor pronóstico desde un punto de vista funcional. Por ello es importante el tratamiento precoz. </p>
                            <p>En algunas ocasiones, disponemos de técnicas de tratamiento encaminadas no solo al control de la infección, sino también a la regeneración de los tejidos periodontales destruidos o la sustitución de dientes no viables por implantes.</p>

                            <h2>Implantes dentales</h2>
                            <p>La implantología es la ciencia que estudia los implantes dentales y, después de una amplia investigación científica, ha dado evidencia a que éstos son la mejor solución para la sustitución permanente de los dientes ausentes. Han hecho posible conseguir resultados fiables que proporcionan una gran mejora estética y funcional al paciente.</p>
                            <p>El implante dental es un tornillo de titanio que es aceptado por el organismo (hueso) como algo propio. De este modo, el hueso crece rápidamente alrededor de él, produciéndose una unión estable y duradera.</p>
                            <p>Para conseguir la reposición de un diente mediante un implante, es preciso colocar el tornillo de titanio en el hueso. Sobre el implante se coloca una corona de cerámica, en la mayoría de los casos, o de resina según el caso.</p>
                            <p>Los implantes pueden sustituir un solo diente, algunos de ellos o todos. Actualmente y en ocasiones que lo permiten, se puede colocar el implante y el diente en el mismo día de la cirugía. Esto es lo que llamamos la técnica de "dientes en un solo día o implante inmediato". Ésta ha sido una gran aportación que ha dado comodidad a los pacientes. Podremos realizar esta técnica dependiendo de la cantidad y calidad del hueso del paciente en la zona a restaurar, así como valorar la estética que requiere la posición a restaurar. </p>
                            <p>Hoy en día el paciente debe saber que con las técnicas de regeneración ósea existentes (elevación del seno maxilar, aumento del reborde alveolar, preservación del alveolo…) podemos colocar implantes dentales con un porcentaje de éxito muy elevado en prácticamente todos los casos.</p>
                            <p>Resultados estéticos y seguros. Los implantes dentales tienen muchas ventajas en comparación con las soluciones tradicionales de coronas, puentes y prótesis removibles:</p>
                            <div class="lists-check">
                                <ul style="padding-left: 20px">
                                    <li>Los implantes dentales se asemejan en función y estética a los dientes naturales. </li>
                                    <li>Los dientes contiguos no son dañados para servir de apoyo a la nueva prótesis. </li>
                                    <li>La colocación de implantes en el hueso evita la reducción ósea que se produce tras la pérdida de los dientes. </b></li>
                                    <li>Se considera un tratamiento definitivo.</li>
                                </ul>
                            </div>
                            <h4>TIEMPOS DE TRATAMIENTO</h4>
                            <p>1º Examinar previamente la situación. Realizar una adecuada exploración y usar los métodos diagnósticos adecuados.</p>
                            <p>2º Inserción del implante dental. En este momento podemos cubrir el implante con encía, si hemos realizado regeneración ósea, o colocar el diente provisional en el mismo momento del implante. Dientes inmediatos o dientes en un día es el nombre que recibe este procedimiento novedoso. </p>
                            <p>3º Sino hemos colocado el diente el mismo día del implante, esperamos el tiempo necesario para que se produzca la integración ósea del implante, 3 meses, y se coloca un pilar de impresión. Se realiza una impresión del pilar ya colocado, se obtiene un modelo final sobre el cual se confecciona la corona perfectamente adaptada al pilar. </p>

                            <h4>¿Son los implantes dentales una solución duradera?</h4>
                            <p>Los implantes dentales son para toda la vida. Ocurre un fenómeno biofísico que produce una unión a nivel molecular del titanio con el hueso, llamado Oseointegración. Dicha unión es tan fuerte que si quisiéramos posteriormente separar el titanio del hueso sería imposible.</p>

                            <h4>¿Es un proceso muy doloroso, y el post-operatorio?</h4>
                            <p>El paciente en ningún momento siente dolor ni molestia alguna. La intervención se lleva a cabo en la mayoría de los casos mediante anestesia local. En intervenciones de larga duración (rehabilitaciones completas, elevaciones de seno maxilar, regeneración ósea) se puede recurrir a la sedación, si el paciente lo prefiere.</p>
                            <p>Al contrario de lo que pueda parecer, en muchos casos el post-operatorio supone menos molestias e incomodidades que las extracciones dentales. Únicamente puede ocasionar pequeñas molestias durante el proceso de cicatrización. En los casos de grandes intervenciones, con regeneraciones óseas e injertos, aparece frecuentemente inflamación durante los primeros días, si bien no resulta dolorosa y permite hacer vida normal.</p>

                            <h4>¿Cuánto tiempo tarda en ponerse un implante dental?</h4>
                            <p>La colocación de un implante unitario suele durar unos treinta minutos. Si sustituimos con implantes dentales todos los dientes, la cirugía puede ser de hasta dos horas.</p>

                            <h4>¿Se requiere algún tipo de cuidado extra con el implante dental?</h4>
                            <p>Deberá tener los mismos cuidados que con sus dientes, una buena higiene diaria y mantenimiento periódico en nuestra consulta.</p>
